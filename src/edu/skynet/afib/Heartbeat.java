package edu.skynet.afib;

public class Heartbeat {

	public Point p, q, r, s, t;
	public float heartrate;

	public Heartbeat(Point p, Point q, Point r, Point s, Point t, float heartrate) {
		this.p = p;
		this.q = q;
		this.r = r;
		this.s = s;
		this.t = t;
		this.heartrate = heartrate;
	}

}
